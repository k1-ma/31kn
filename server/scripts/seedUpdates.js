/**
 * Run the seeding process.
 * 
 * @param {Object} options - Options object
 * @param {Object} [options.existingPool] - Existing database pool to use (will not be closed)
 * @param {boolean} [options.exitOnComplete] - Whether to call process.exit on completion (CLI mode)
 * @returns {Promise<{inserted: number, skipped: number}>} - Seeding results
 */
export async function runSeedUpdates({ existingPool, exitOnComplete = false } = {}) {
  // eslint-disable-next-line no-console
  console.log("[seed:updates] Starting updates seeding...");

  const ownPool = !existingPool;
  const pool = existingPool || new Pool({
    connectionString: process.env.DATABASE_URL,
    ssl: process.env.DATABASE_SSL === "true" ? { rejectUnauthorized: false } : false,
  });

  try {
    // Check if project_updates table exists
    const tableCheck = await pool.query(`
      SELECT EXISTS (
        SELECT FROM information_schema.tables 
        WHERE table_name = 'project_updates'
      );
    `);
    
    if (!tableCheck.rows[0].exists) {
      const msg = "Table 'project_updates' does not exist. Run migrations first.";
      // eslint-disable-next-line no-console
      console.error("[seed:updates] ✗ " + msg);
      if (exitOnComplete) {
        process.exit(1);
      }
      throw new Error(msg);
    }

    // Get existing update titles to avoid duplicates
    const existingResult = await pool.query("SELECT title FROM project_updates");
    const existingTitles = new Set(existingResult.rows.map((r) => r.title));

    let insertedCount = 0;
    let skippedCount = 0;

    for (const update of UPDATES) {
      if (existingTitles.has(update.title)) {
        skippedCount++;
        continue;
      }

      await pool.query(
        `INSERT INTO project_updates (title, description, category, version, is_published, published_at, created_at)
         VALUES ($1, $2, $3, $4, true, $5, $5)`,
        [
          update.title,
          update.description || null,
          update.category || "Other",
          update.version || null,
          new Date(update.published_at + "T12:00:00Z"),
        ]
      );
      insertedCount++;
    }

    // eslint-disable-next-line no-console
    console.log(`[seed:updates] ✓ Seeding completed:`);
    // eslint-disable-next-line no-console
    console.log(`[seed:updates]   - Inserted: ${insertedCount} updates`);
    // eslint-disable-next-line no-console
    console.log(`[seed:updates]   - Skipped (already exist): ${skippedCount} updates`);

    // Only close the pool if we created it
    if (ownPool) {
      await pool.end();
      // eslint-disable-next-line no-console
      console.log("[seed:updates] ✓ Database connection closed");
    }

    if (exitOnComplete) {
      process.exit(0);
    }

    return { inserted: insertedCount, skipped: skippedCount };
  } catch (err) {
    // eslint-disable-next-line no-console
    console.error("[seed:updates] ✗ Seeding failed:", err?.message || err);
    if (err?.stack) {
      // eslint-disable-next-line no-console
      console.error("[seed:updates] Stack:", err.stack);
    }
    // Only close the pool if we created it
    if (ownPool) {
      await pool.end();
    }
    if (exitOnComplete) {
      process.exit(1);
    }
    throw err;
  }
}

